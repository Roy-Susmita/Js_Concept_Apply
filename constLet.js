// let can be used as variable which value can be changed.
// const can be used as a variable which value can not be changed.
// let and const was introduce in ES6.earlier upto ES5 var was used as a variable.
var name = "lee min hoo";
console.log(name.length);
const country = "Bangladesh";
country = "South korea";
console.log(country);

let country1 = "Bangladesh";
country1 = "South korea";
console.log(country1);
